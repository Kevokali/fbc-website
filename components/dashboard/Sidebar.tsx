export default function Sidebar() {
  const [user, setUser] = useState<User | null>(null)
  const [isCollapsed, setIsCollapsed] = useState(false)
  const pathname = usePathname()

  useEffect(() => {
    const loadUser = async () => {
      const currentUser = await getCurrentUser()
      setUser(currentUser)
    }
    loadUser()
  }, [])

  if (!user) return null

  const menuItems = user.role === 'admin' ? adminMenuItems : clientMenuItems

  return (
    <aside
      className={`fixed left-0 top-16 bottom-0 bg-white border-r border-soft-grey transition-all duration-300 z-40 ${
        isCollapsed ? 'w-20' : 'w-64'
      }`}
    >
      {/* Collapse Toggle */}
      <button
        onClick={() => setIsCollapsed(!isCollapsed)}
        className="absolute -right-3 top-4 w-6 h-6 bg-deep-blue text-white rounded-full flex items-center justify-center shadow-md hover:bg-navy transition-colors z-10"
      >
        <svg
          className={`w-4 h-4 transition-transform ${isCollapsed ? 'rotate-180' : ''}`}
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
        >
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
        </svg>
      </button>

      {/* Menu Items */}
      <nav className="p-4 space-y-2 h-full overflow-y-auto">
        {menuItems.map((item) => {
          const isActive = pathname === item.href
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                isActive
                  ? 'bg-emerald/10 text-emerald border-l-4 border-emerald'
                  : 'text-text-secondary hover:bg-light-grey hover:text-deep-blue'
              }`}
              title={isCollapsed ? item.name : undefined}
            >
              <span className="flex-shrink-0">{item.icon}</span>
              {!isCollapsed && <span className="font-medium">{item.name}</span>}
            </Link>
          )
        })}
      </nav>

      {/* Security Notice */}
      {!isCollapsed && (
        <div className="absolute bottom-4 left-4 right-4 p-3 bg-light-grey rounded-lg border border-soft-grey">
          <p className="text-xs text-text-secondary">
            <strong className="text-deep-blue">Security:</strong> We do not store client system passwords.
          </p>
        </div>
      )}
    </aside>
  )
}
